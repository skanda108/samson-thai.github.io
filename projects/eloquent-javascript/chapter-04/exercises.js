////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(a, b, step) {
  let arr = [];
  if (a === b){
    return arr;
  }
  if (step === undefined){
    step = 1;
  }
  if (step >= 1){
    for (let i = a; i <= b; i += step){
      arr.push(i);
    }
  } else {
    for (let i = a; i >=b; i += step){
      arr.push(i);
    }
  }

  return arr;
}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
  if (array === []){
    return 0;
  }
  let total = 0;
  for (let i = 0; i < array.length; i++){
    total += array[i];
  }
  return total;

}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let out = [];
  for (let i = 0; i < array.length; i++){
    out.unshift(array[i]);
  }
  return out;

}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  let leftSide = 0;
  let rightSide = array.length - 1;

  while (leftSide < rightSide){
    let old = array[leftSide];
    array[leftSide] = array[rightSide];
    array[rightSide] = old;

    leftSide++;
    rightSide--;
  }
  return array;
}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
  let rest = null;

  //iterate through input array
  for (let i = array.length - 1; i >= 0; i--){
    rest = { value: array[i], rest: rest };
  }

  return rest;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list, array=[]) {
  //base
  if (list.rest === null){
    array.push(list.value);
    return array;
  }
  //recursion
  array.push(list.value);

  return listToArray(list.rest, array);
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(value, list) {
  return {value, rest: list};
}

////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, n) {
  if (!list){
    return undefined;
  } else if (n === 0){
    return list.value;
  } else {
    return nth(list.rest, n - 1);
  }

}

////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(x, y) {
  //determine if x and y are BOTH no considered objects
  if(typeof x !== 'object' && typeof y !== 'object'){
    return x === y;
  }
  //determine if x OR y is not an object
  if (typeof x !== 'object' || typeof y !== 'object'){
    return false;
  }
  // x and are both complex pieces of data
  let xKeys = Object.keys(x);
  let yKeys = Object.keys(y);
  //determin if the lengths are not equal
  if (xKeys.length !== yKeys.length){
    return false;
  }
  //iterate through the array of keys to make sure the keys and values match
  for (let i = 0; i < xKeys.length; i++){
    if (!yKeys.includes(xKeys[i]) || !deepEqual(x[xKeys[i]], y[xKeys[i]])){
      return false;
    }
  }

  return true;
}

