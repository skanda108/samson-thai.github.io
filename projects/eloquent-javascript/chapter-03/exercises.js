////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2){
    return num1;
} else if (num1 > num2){
    return num2;
} else {
    return num1;
}

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num % 2 === 0){
    return true;
} else {
    return false;
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(word, check) {
  let count = 0;
  for (let i = 0; i < word.length; i++){
      if (word[i] === check){
          count++;
      }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(word) {
  let count = 0;
  for (let i = 0; i < word.length; i++){
      if (word[i] === 'B'){
          count++;
      }
  }
  return count;
}

